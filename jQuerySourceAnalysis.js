jQuery.extend({
	// Is the DOM ready to be used? Set to true once it occurs.
	//当前文档是否已经ready了
	isReady: false,

	// A counter to track how many items to wait for before
	// the ready event fires. See #6781
	//可以使用holdReady接口来hold住ready事件
	readyWait: 1,

	// Hold (or release) the ready event
	//可以使用holdReady接口来hold住ready事件
    //参数hold为true时，jQuery会hold住ready回调。
    //某个地方调用了holdReady()，参数hold为false，jQuery会把hold住的次数减1
	holdReady: function( hold ) {
		if ( hold ) {
			jQuery.readyWait++;
		} else {
			jQuery.ready( true );
		}
	},

	// Handle when the DOM is ready
	ready: function( wait ) {
		//如果不需要等待，holdReady()的时候，把hold住的次数减1，如果还没到达0，说明还需要继续hold住，return掉
		//如果不需要等待，判断是否已经Ready过了，如果已经ready过了，就不需要处理了。异步队列里边的done的回调都会执行了
		// Abort if there are pending holds or we're already ready
		if ( wait === true ? --jQuery.readyWait : jQuery.isReady ) {
			return;
		}

		// Remember that the DOM is ready
		//此时Ready了
		jQuery.isReady = true;

		// If a normal DOM Ready event fired, decrement, and wait if need be		
		//当时如果此时有某个地方hold住了，那就要继续等待
		if ( wait !== true && --jQuery.readyWait > 0 ) {
			return;
		}

		// If there are functions bound, to execute
		//调用异步队列，然后派发成功事件出去（最后使用done接收，详细见《jQuery源码剖析（三）——$.Deferred》），把上下文切换成document，默认第一个参数是jQuery。
		//还记得 $(document).ready(function($){})这里的$不？
		readyList.resolveWith( document, [ jQuery ] );

		// Trigger any bound ready events
		//最后jQuery还可以触发自己的ready事件
		//例如：
		//    $(document).on('ready', fn2);
		//    $(document).ready(fn1);
		//这里的fn1会先执行，自己的ready事件绑定的fn2回调后执行
		if ( jQuery.fn.triggerHandler ) {
			jQuery( document ).triggerHandler( "ready" );
			jQuery( document ).off( "ready" );
		}
	}
});

