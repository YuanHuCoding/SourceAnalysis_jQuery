// Multifunctional method to get and set values of a collection
// The value/s can optionally be executed if it's a function
var access = jQuery.access = function( elems, fn, key, value, chainable, emptyGet, raw ) {
	var i = 0,
		len = elems.length,
		bulk = key == null;//此处的 bulk 指的是批量操作，key 为 null，就是说当前的操作即……

	// Sets many values
	//key 为对象，就表明这是一个类似于 { height: 100, width: 200 } 的键值对，是将多步操作合并在了一起，
	//而且这样的操作不能是 get 类方法，因为你没法确定最终的返回值是什么，所以 chainable 设置为 true，并且循环 key 对象来分别调用 access 方法。
	if ( jQuery.type( key ) === "object" ) {
		chainable = true;
		for ( i in key ) {
			jQuery.access( elems, fn, i, key[i], true, emptyGet, raw );
		}

	// Sets one value
	//value 存在，表明是 set 类方法，所以依然是允许链式调用。raw 的含义上面介绍过了。
	} else if ( value !== undefined ) {
		chainable = true;

		if ( !jQuery.isFunction( value ) ) {
			raw = true;
		}

		//如果我能找到 bulk 的真实例子，那么对上面代码可能就会更好的理解了……
		if ( bulk ) {
			// Bulk operations run against the entire set
			// 批量操作针对整个集合来运行
			if ( raw ) {
				fn.call( elems, value );
				fn = null;

			// ...except when executing function values
			// 除非 value 是个函数
			} else {
				bulk = fn;
				fn = function( elem, key, value ) {
					return bulk.call( jQuery( elem ), value );
				};
			}
		}

		if ( fn ) {
			for ( ; i < len; i++ ) {
				fn( elems[i], key, raw ? value : value.call( elems[i], i, fn( elems[i], key ) ) );
			}
		}
	}

	//好的，终于到了函数结尾处。注意前面处理的都是 set 类方法，包括设置一个值或设置多个值，那么 get 类方法呢？嘿嘿，它们都可怜巴巴的即在 return 里了。
	//通过前面的源码能看到，chainable 被设置成了 true，链式调用返回的就是元素集合本身。
	//如果不是链式调用，那么可认为是 get 类方法。
	//bulk 为 true，说明需要在所有 elems 上调用 fn，它的操作结果就是返回值。
	//接下来判断 length，即元素集合中元素的个数，如果集合为空，则返回默认值 emptyGet，否则返回集合中第一个元素与 key 传入 fn。
	return chainable ?
		elems :

		// Gets
		bulk ?
			fn.call( elems ) :
			len ? fn( elems[0], key ) : emptyGet;
};


