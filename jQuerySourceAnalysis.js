// jQuery.extend( target, [ object1 ], [ objectN ] )
// jQuery.extend( [ deep ], target, object1, [ objectN ] )
jQuery.extend = jQuery.fn.extend = function() {
	var options, name, src, copy, copyIsArray, clone,
		target = arguments[0] || {},
		i = 1,
		length = arguments.length,
		deep = false;

	// Handle a deep copy situation
	// 如果第一个参数是boolean型，可能是深度拷贝
	if ( typeof target === "boolean" ) {
		deep = target;

		// Skip the boolean and the target
		target = arguments[ i ] || {};
		i++;// 跳过boolean和target，从第3个开始，此时i=2
	}

	// Handle case when target is a string or something (possible in deep copy)
	// 如果target不是对象也不是函数，则强制设置为空对象
	if ( typeof target !== "object" && !jQuery.isFunction(target) ) {
		target = {};
	}

	// Extend jQuery itself if only one argument is passed
	// 如果只传入一个参数，则认为是对jQuery扩展
	if ( i === length ) {
		target = this;
		i--;//此时i=1
	}

	for ( ; i < length; i++ ) {
		// Only deal with non-null/undefined values
		//只处理非空参数
		if ( (options = arguments[ i ]) != null ) {
			// Extend the base object
			for ( name in options ) {
				src = target[ name ];
				copy = options[ name ];

				// Prevent never-ending loop
				// 避免循环引用
				if ( target === copy ) {
					continue;
				}

				// Recurse if we're merging plain objects or arrays
				// 深度拷贝且值是纯对象或数组，则递归
				if ( deep && copy && ( jQuery.isPlainObject(copy) || (copyIsArray = jQuery.isArray(copy)) ) ) {
					if ( copyIsArray ) {// 如果copy的是数组
						copyIsArray = false;
						clone = src && jQuery.isArray(src) ? src : [];// clone为src的修正值

					} else {//如果copy的是纯对象
						clone = src && jQuery.isPlainObject(src) ? src : {};// clone为src的修正值
					}

					// Never move original objects, clone them
					// 递归调用jQuery.extend
					target[ name ] = jQuery.extend( deep, clone, copy );

				// Don't bring in undefined values
				// 不能拷贝空值
				} else if ( copy !== undefined ) {
					target[ name ] = copy;
				}
			}
		}
	}

	// Return the modified object
	//返回更改后的对象
	return target;
};

